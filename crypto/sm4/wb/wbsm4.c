void wbsm4_set_key(const uint8_t *key, void *ctx, size_t len_ctx)
{
    uint8_t *p, *end;

    DECLARE_IS_ENDIAN;

    memcpy(ctx, key, len_ctx);
    if (IS_LITTLE_ENDIAN)
        return;

    p = (uint8_t *)ctx;
    if (len_ctx == sizeof(wbsm4_xiao_dykey_context)) {
        end = (uint8_t*)(((wbsm4_xiao_dykey_context*)ctx)->Xor32Table);
    }
    else {
        end = (uint8_t*)(((wbsm4_xiao_dykey_ctxrk*)ctx)->R);
    }
    while (p < end) {
        uint8_t t;
        t = p[0];
        p[0] = p[3];
        p[3] = t;

        t = p[1];
        p[1] = p[2];
        p[2] = t;

        p += 4;
    }
}

void wbsm4_export_key(const void *ctx, uint8_t *key, size_t len_ctx)
{
    uint8_t *p, *end;

    DECLARE_IS_ENDIAN;

    memcpy(key, ctx, len_ctx);
    if (IS_LITTLE_ENDIAN)
        return;

    p = (uint8_t *)key;
    if (len_ctx == sizeof(wbsm4_xiao_dykey_context)) {
        end = (uint8_t*)(((wbsm4_xiao_dykey_context*)ctx)->Xor32Table);
    }
    else {
        end = (uint8_t*)(((wbsm4_xiao_dykey_ctxrk*)ctx)->R);
    }
    end = p + (end - (uint8_t *)ctx);
    while (p < end) {
        uint8_t t;
        t = p[0];
        p[0] = p[3];
        p[3] = t;

        t = p[1];
        p[1] = p[2];
        p[2] = t;

        p += 4;
    }
}

static void gen_Bijection4pair(uint8_t *table, uint8_t *inverse_table)
{
    uint8_t buff_table_entry;
    int i, r;
    for (i = 0; i < 16; i++) {
        table[i] = (uint8_t)i;
    }

    for (i = 0; i < 16; i++) {
        r = (i + wb_xiao_dy_cus_random() % 16) % 16;
        buff_table_entry = table[i];
        table[i] = table[r];
        table[r] = buff_table_entry;
    }

    /*  generate inverse mapping of bijection */
    for (i = 0; i < 16; i++) {
        inverse_table[table[i]] = (uint8_t)i;
    }
}

void gen_Bijection32pair(Biject32 *bij, Biject32 *bij_inv)
{
    int i;
    for (i = 0; i < 8; i++)
    {
        gen_Bijection4pair(bij->lut[i], bij_inv->lut[i]);  /*  generate bijection and its inverse mapping */
    }
}

uint32_t BijectionU32(const Biject32* bij, uint32_t x)
{
    uint32_t result = 0;
    uint8_t transformed;
    int i;
    for (i = 0; i < 8; i++) {
        transformed = bij->lut[i][(x >> (28 - i * 4)) & 0x0F];
        result |= transformed << (28 - i * 4);
    }
    return result;
}
